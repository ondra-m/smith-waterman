#include <fstream>
#include <string>

#include "dna_file.h"

using namespace std;

// -------------------------------------------------------------------------------------------

DNAFile::DNAFile(){}

// -------------------------------------------------------------------------------------------

DNAFile::~DNAFile(){ 
  file.close();
}

// -------------------------------------------------------------------------------------------

void DNAFile::set(char * file_name){
  this -> file_name = file_name;
}

// -------------------------------------------------------------------------------------------

bool DNAFile::open(){
  file.open(file_name, std::ifstream::in);
  return is_open();  
}

// -------------------------------------------------------------------------------------------

bool DNAFile::is_open(){
  return file.is_open();
}

// -------------------------------------------------------------------------------------------

bool DNAFile::is_eof(){
  return file.eof();
}

// -------------------------------------------------------------------------------------------

void DNAFile::close(){
  file.close();
}

// -------------------------------------------------------------------------------------------

string DNAFile::get_line(){
  string tmp;

  do{
    getline(file, tmp);
  } while(tmp[0] == '#');

  return tmp;
}

